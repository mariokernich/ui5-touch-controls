/*
 * Syncs the simple-keyboard stylesheet from node_modules into the library
 * theme so it ships as part of library.css.
 *
 * The UI5 build tooling (ui5-tooling-transpile) cannot process an ES
 * `import "...css"` statement, so instead of importing the CSS at runtime we
 * vendor its content into src/themes/base/Keyboard.less.
 * This script keeps that vendored copy in sync with the version currently
 * installed in node_modules and runs automatically before `build` and `start`.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");

const SOURCE = resolve(
	projectRoot,
	"node_modules/simple-keyboard/build/css/index.css",
);
const TARGET = resolve(
	projectRoot,
	"src/themes/base/Keyboard.less",
);

const HEADER = `/*!
 * simple-keyboard styles - AUTO-GENERATED, DO NOT EDIT BY HAND.
 * Source: simple-keyboard/build/css/index.css (MIT License)
 * Regenerated by scripts/sync-keyboard-css.mjs on build/serve.
 */
`;

try {
	const css = readFileSync(SOURCE, "utf8");
	writeFileSync(TARGET, `${HEADER}\n${css.trimStart()}\n`, "utf8");
	console.log(
		`[sync-keyboard-css] Copied ${SOURCE} -> ${TARGET}`,
	);
} catch (err) {
	console.error(
		`[sync-keyboard-css] Failed to copy simple-keyboard CSS: ${err.message}`,
	);
	console.error(
		"[sync-keyboard-css] Make sure dependencies are installed (pnpm install).",
	);
	process.exit(1);
}
